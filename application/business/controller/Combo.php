    /**
     * 添加套餐
     */
    public function addPackage()
    {
        $result = $this->_form($this->table);

        if(isset($result['id'])){
            $this->title = '编辑套餐';
        }else {
            $this->title = '添加套餐';
        }

        return $this->_form($this->table, 'packageform', null, []);

    }
    /**
     * 添加套餐 返回结果跳转处理
     * @param $result
     */
    protected function _addPackage_form_result(&$result)
    {
        $result !== false ? $this->success('恭喜，保存成功哦！', url('/').'business.html#'.url('package').'?type='.$this->request->get('type').'&spm='.$this->spm) : $this->error('保存失败，请稍候再试！');
    }

