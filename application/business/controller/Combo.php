    /**
     * 套餐列表
     */
    public function Package()
    {
        $get = $this->request->get();

        if($get['type']==0){
            $this->title = '碑文套餐列表';
        }elseif ($get['type']==1){
            $this->title = '安葬套餐列表';
        }else{
            $this->title = '寄存套餐列表';
        }
        // 实例Query对象
        $db = Db::name($this->table)->where(['type'=>$get['type'],'create_by'=>$this->uid]);
        // 应用搜索条件
        foreach (['name', 'desc'] as $key) {
            if (isset($get[$key]) && $get[$key] !== '') {
                $db->where($key, 'like', "%{$get[$key]}%");
            }
        }

        parent::_list($db);
    }


